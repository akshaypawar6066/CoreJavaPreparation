package Day05_LoopsAndJumpStatemenetsInJava;

public class WhileLoopDemo {
	public static void main(String[] args) {
		//Print 1 to 10 Numbers
		int i=1;       //Initializaton
		while(i <=10) {         //Condition
			System.out.print(i+",");
			i++;                   //Increment/decrement
		}
	}

}
